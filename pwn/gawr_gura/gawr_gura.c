            puts("My age is");
            printf("%d\n", Gawr_grua.age);
            puts("A");
            break;

        case 5:
            puts("What do you want to say?");
            read(0, Gawr_grua.note, 0x30);
            break;

        case 6:
            puts("You say: ");
            printf("%s", Gawr_grua.note);
            break;

        case 7:
            exit(0);
            break;

        default:
