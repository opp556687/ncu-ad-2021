void menu()
{
    puts("Enter 1 ~ 4 to know more information.");
    puts("1.) Name");
    puts("2.) Avatar");
    puts("3.) Height");
    puts("4.) Age");
    puts("5.) Talking thing to her.");
    puts("6.) Print your message.");
    puts("7.) exit");
}

int read_int()
{
    char buf[0x10];
    read(0, buf, 0x10);
    return atoi(buf);
}

